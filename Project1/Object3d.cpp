Object3d* Object3d::Create(XMFLOAT3 position,Model* model)
{
	// 3Dオブジェクトのインスタンスを生成
	Object3d* object3d = new Object3d(position,model);
	if (object3d == nullptr) {
		return nullptr;
	}

	// 初期化
	if (!object3d->Initialize()) {
		delete object3d;
		assert(0);
		return nullptr;
	}
	return object3d;
}

